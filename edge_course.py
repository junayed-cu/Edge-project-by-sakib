# sort vlaue using nested loop
def bubble_sort(arr):
  n=len(arr)
  for i in range(n):
    for j in range(0,n-i-1):
      if arr[j]>arr[j+1]:
        arr[j],arr[j+1]=arr[j+1] ,arr[j]
  return arr
arr=[64,34,25,12,12,22,11,90]
print(bubble_sort(arr))

def sort(arr):
  sorted_arr=[]
  n=len(arr)
  for i in range(n):
    minimum_vlaue=min(arr)
    arr.remove(minimum_vlaue)
    sorted_arr.append(minimum_vlaue)
  return sorted_arr
arr=[64,34,25,12,12,22,11,90]
